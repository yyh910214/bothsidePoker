package com.apolloners.poker.vo;

public class Card {
	private int front;
	private int back;
	/**
	 * @return the front
	 */
	public int getFront() {
		return front;
	}
	/**
	 * @param front the front to set
	 */
	public void setFront(int front) {
		this.front = front;
	}
	/**
	 * @return the back
	 */
	public int getBack() {
		return back;
	}
	/**
	 * @param back the back to set
	 */
	public void setBack(int back) {
		this.back = back;
	}
	
	
}
